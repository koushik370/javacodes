public class ifelse {
    public static void main(String[] args) {
        int x=35;
        int y=40;
        int z=30;
        if (x>y && x>z){
            System.out.println("hello x is big");
        }
        else if(y>z){
            System.out.println("hello y is big");
        }
        else{
            System.out.println("hello z is big");
        }
    }
}
